-- Oracle schema for SERVICE_METRICS
CREATE TABLE SERVICE_METRICS (
  ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  SERVICE_NAME VARCHAR2(200),
  ENDPOINT VARCHAR2(1000),
  HEALTHY NUMBER(1),
  LATENCY_MS NUMBER,
  CHECKED_AT TIMESTAMP
);

CREATE INDEX IDX_SERVICE_METRICS_CHECKED_AT ON SERVICE_METRICS (CHECKED_AT);
